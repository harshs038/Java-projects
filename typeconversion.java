public class typeconversion {
    public static void main(String[] args) {
        float number = 5;
        System.out.println(number);
        // here the answer is 5.0 because type conversion
        // 5l here the meaning of l is long but answer is same
        // here for convert float into int is given below
        int number1 = (int) 5.0;
        System.out.println(number1);
    }
}