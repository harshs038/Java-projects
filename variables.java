public class variables {
    public static void main(String[] args) {
        // variables declration program
        int number;
        number = 865;
        System.out.println(number);
        // here we can update the value of a variable and print the latest value
        number = 45;
        System.out.println(number);

        // float datatype
        float age = 18.0f;
        System.out.println(age);

        // double datatype (f is not required in duble datatype)
        double age2 = 18.0;
        System.out.println(age2);

        // string datatype always in double cores
        String name = "HARSH";
        System.out.println(name);

        // boolean datatype
        boolean age18 = true;
        System.out.println(age18);
    }
}